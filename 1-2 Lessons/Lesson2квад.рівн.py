a = int(input("Вкажіть змінну a"))
b = int(input("Вкажіть змінну b"))
c = int(input("Вкажіть змінну c"))

D = (b**2)-4*a*c

if D > 0:
    x = (-b+D**0.5)/2*a
    y = (-b-D**0.5)/2*a
    print ("Дискримінант дорівнює",D)

    print ("X1=",x)
    print ("X2=",y)

elif 0 <= D <= 0:
    x = (-b)/2*a
    print ("Дискримінант дорівнює",D)
    print ("X=",x)

else:
    print ("Дискримінант дорівнює",D)
    print ("Дискримінант відєммний тому рівняння розвязків немає")
